/**
 * تابع اصلی دریافت رژیم
 * @param {number} age
 * @param {string} gender - "male" | "female"
 * @param {number} bmi
 * @param {string} disease - "diabetes" | "hypertension" | "highcholesterol"
 * @returns {object} - اطلاعات رژیم
 */
function getDiet(age, gender, bmi, disease) {
  // تعیین گروه سنی
  let ageGroup;
  if (age >= 12 && age <= 17)      ageGroup = "12-17";
  else if (age >= 18 && age <= 30) ageGroup = "18-30";
  else if (age >= 31 && age <= 50) ageGroup = "31-50";
  else if (age >= 51 && age <= 65) ageGroup = "51-65";
  else if (age >= 66)              ageGroup = "66+";
  else return null;

  // تعیین دسته BMI
  let bmiCategory;
  if (bmi < 18.5)       bmiCategory = "underweight";
  else if (bmi < 25)    bmiCategory = "normal";
  else if (bmi < 30)    bmiCategory = "overweight";
  else                  bmiCategory = "obese";

  try {
    return DIET_DATA[ageGroup][disease][bmiCategory][gender];
  } catch(e) {
    return null;
  }
}
